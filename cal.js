// For loop
// from where it will start -> 0
// where it will end -> 5
// how much it will increment -> 0+1, 1+1, 2+1, 3+1, 4+1, 5+1

// i = 0, i++ -> 1, i++ -> 2, i++ -> 3, i++ -> 4, i++ -> 5, i++ -> 6 -> increment
// i = 5, i-- -> 4, i-- -> 3, i-- -> 2, i-- -> 1, i-- -> 0, i-- -> -1 -> Decrement

